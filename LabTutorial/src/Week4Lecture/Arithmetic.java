package Week4Lecture;

public class Arithmetic {
	public static void main (String[] args) {
		// arthimatic
		System.out.println(1 + 1); // overloaded
		System.out.println("hello" + 1 + 1);
		System.out.println("hello" + (1 + 1));
		
		int a = 99 / 10;
		System.out.println(a);
		
		double b = 99 / 10;
		System.out.println(b);
		
		double c = 99.0 / 10.0;
		System.out.println(c);
	}
}
